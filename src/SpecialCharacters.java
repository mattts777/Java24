class SpecialCharacters{
    public static void main(String[] args){
        System.out.println("\'test\'"); // Singl quote
        System.out.println("\"test\""); // Double quote
        System.out.println("\\test\\"); // Backslash
        System.out.println("\t test \t"); // Tab
        System.out.println("\b test \b"); // Backspace
        System.out.println("\r test \r"); // Carriage Return
        System.out.println("\f test \f"); // Form feed
        System.out.println("This is a:\ntest"); // New line
    }
}